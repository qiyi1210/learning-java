//输出一个三角形
package day4.Circulation;

public class RectangleDemo {
    public static void main(String[] args) {
        for (int j = 1; j <= 10; j++) {

            for (int i = 1; i <= j; i++) {
                System.out.print(" *");
            }
            System.out.println();
        }
    }
}
